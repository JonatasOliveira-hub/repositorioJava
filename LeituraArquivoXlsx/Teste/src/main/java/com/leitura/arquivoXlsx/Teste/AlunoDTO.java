package com.leitura.arquivoXlsx.Teste;

/*Classe que modela os dados do arquivo de entrada*/
public class AlunoDTO {

	private String enderecoEmail;
	private String nome;
	private String planoDeDesconto;
	
	public String getEnderecoEmail() {
		return enderecoEmail;
	}
	public void setEnderecoEmail(String enderecoEmail) {
		this.enderecoEmail = enderecoEmail;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getPlanoDeDesconto() {
		return planoDeDesconto;
	}
	public void setPlanoDeDesconto(String planoDeDesconto) {
		this.planoDeDesconto = planoDeDesconto;
	}
	
	

}
